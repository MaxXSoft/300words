    <h1>什么是<i>故事接龙</i>？</h1>
    <p>顾名思义，故事接龙类似于成语接龙。当一个人写出一篇短故事之后，其他人可以在此基础上接着讲另一个故事。以此类推，形成一个逐渐变长的故事串。</p>
    <p>接龙的起点叫做根故事（root story），每个故事可以拥有多个接龙而成的子分支，最终接龙而成的故事会组成一棵以根故事为根节点的故事树（story tree）。</p>
    <p>此网站允许用户发布或者接龙 300 字以内的短故事，所以得名“300字”。</p>
